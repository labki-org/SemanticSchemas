<?php

namespace MediaWiki\Extension\SemanticSchemas\Generator;

use MediaWiki\Extension\SemanticSchemas\Schema\CategoryModel;
use MediaWiki\Extension\SemanticSchemas\Schema\InheritanceResolver;
use MediaWiki\Extension\SemanticSchemas\Schema\PropertyModel;
use MediaWiki\Extension\SemanticSchemas\Schema\SubobjectModel;
use MediaWiki\Extension\SemanticSchemas\Store\PageCreator;
use MediaWiki\Extension\SemanticSchemas\Store\WikiPropertyStore;
use MediaWiki\Extension\SemanticSchemas\Store\WikiSubobjectStore;
use MediaWiki\Extension\SemanticSchemas\Util\NamingHelper;

/**
 * FormGenerator
 * --------------
 * Generates PageForms markup for category creation/editing forms.
 *
 * Features:
 * - Automatic field generation from CategoryModel schema
 * - Required and optional property sections
 * - Subobject repeatable blocks with minimum instances
 * - Namespace support for page creation
 * - Automatic hierarchy preview (when parent category property detected)
 */
class FormGenerator {

	private PageCreator $pageCreator;
	private WikiPropertyStore $propertyStore;
	private PropertyInputMapper $inputMapper;
	private WikiSubobjectStore $subobjectStore;

	public function __construct(
		PageCreator $pageCreator,
		WikiPropertyStore $propertyStore,
		PropertyInputMapper $inputMapper,
		WikiSubobjectStore $subobjectStore
	) {
		$this->pageCreator = $pageCreator;
		$this->propertyStore = $propertyStore;
		$this->inputMapper = $inputMapper;
		$this->subobjectStore = $subobjectStore;
	}

	private function s( ?string $v ): string {
		return $v ?? '';
	}

	/**
	 * Generate the full PageForms form for a category.
	 *
	 * When an inheritance chain is provided, fields are grouped under
	 * category subheadings (root-first order). For root categories
	 * with no parents, no redundant heading is rendered.
	 *
	 * @param CategoryModel $leafCategory The leaf (unmerged) category
	 * @param CategoryModel[] $inheritanceChain C3-linearized chain [child, parent1, ..., root]
	 * @return string
	 */
	public function generateForm( CategoryModel $leafCategory, array $inheritanceChain = [] ): string {
		$name = trim( $leafCategory->getName() );
		$label = trim( $leafCategory->getLabel() );

		if ( $name === '' ) {
			throw new \InvalidArgumentException( "Category name cannot be empty" );
		}
		if ( $label === '' ) {
			throw new \InvalidArgumentException( "Category label cannot be empty" );
		}

		// Default: single-element chain for backward compatibility
		if ( $inheritanceChain === [] ) {
			$inheritanceChain = [ $leafCategory ];
		}

		$lines = [];

		/* ----------------------------------------------------------
		 * <noinclude> Header
		 * -------------------------------------------------------- */
		$lines[] = '<noinclude>';
		$lines[] = '<!-- AUTO-GENERATED by SemanticSchemas - DO NOT EDIT MANUALLY -->';
		$lines[] = '<!-- This form is automatically regenerated -->';
		$lines[] = 'This is the form for editing [[:Category:' . $this->s( $name ) . ']] pages.';

		$formInput = [
			'form=' . $this->s( $name ),
			'autocomplete on category=' . $this->s( $name ),
		];

		if ( $leafCategory->getTargetNamespace() !== null ) {
			$formInput[] = 'namespace=' . $this->s( $leafCategory->getTargetNamespace() );
		}

		$lines[] = '{{#forminput:' . implode( '|', $formInput ) . '}}';

		$lines[] = '</noinclude><includeonly>';
		$lines[] = '';

		/* ----------------------------------------------------------
		 * Page name info (for namespace targeting)
		 * -------------------------------------------------------- */
		if ( $leafCategory->getTargetNamespace() !== null ) {
			$lines[] = '{{{info|page name=' . $this->s( $leafCategory->getTargetNamespace() ) . ':<page name>}}}';
			$lines[] = '';
		}

		/* ----------------------------------------------------------
		 * Template binding
		 * -------------------------------------------------------- */
		$lines[] = '{{{for template|' . $this->s( $name ) . '}}}';
		$lines[] = '';

		/* ----------------------------------------------------------
		 * Property fields grouped by origin category
		 * -------------------------------------------------------- */
		$lines = array_merge( $lines, $this->generateGroupedPropertyFields( $inheritanceChain ) );

		$lines[] = '{{{end template}}}';
		$lines[] = '';

		/* ----------------------------------------------------------
		 * Hierarchy Preview (if parent category property exists)
		 * -------------------------------------------------------- */
		$allPropsForDetection = array_unique( array_merge(
			...array_map( static fn ( $cat ) => $cat->getAllProperties(), $inheritanceChain )
		) );
		$parentProp = $this->findParentCategoryPropertyFromList( $allPropsForDetection );
		if ( $parentProp !== null ) {
			$lines[] = '{{#semanticschemas_load_form_preview:}}';
			$lines[] = '';

			$parentParam = NamingHelper::propertyToParameter( $parentProp );

			$lines[] = '<!-- Auto-populated category membership field -->';
			$lines[] = '{{{standard input|free text|hidden|rows=1'
				. '|placeholder=Parent categories will be added automatically|id=ss-parent-categories}}}';
			$lines[] = '';

			$lines[] = "'''Hierarchy Preview:'''";
			$lines[] = '<div id="ss-form-hierarchy-preview" data-parent-field="'
				. $this->s( $parentParam ) . '"></div>';
			$lines[] = '';
		}

		/* ----------------------------------------------------------
		 * Subobject sections — collect from the full chain
		 * -------------------------------------------------------- */
		$lines = array_merge( $lines, $this->generateSubobjectSectionsFromChain( $inheritanceChain ) );

		/* ----------------------------------------------------------
		 * Free text section
		 * -------------------------------------------------------- */
		$lines[] = "'''Free text:'''";
		$lines[] = '';
		$lines[] = '{{{standard input|free text|rows=10}}}';
		$lines[] = '';

		/* ----------------------------------------------------------
		 * Summary + Buttons
		 * -------------------------------------------------------- */
		$lines[] = '{{{standard input|summary}}}';
		$lines[] = '';
		$lines[] = '{{{standard input|save}}} '
			. '{{{standard input|preview}}} '
			. '{{{standard input|changes}}} '
			. '{{{standard input|cancel}}}';
		$lines[] = '</includeonly>';

		return implode( "\n", $lines );
	}

	/**
	 * Generate property fields grouped by origin category.
	 *
	 * Iterates the inheritance chain root-first so the most general category's
	 * fields appear at the top. Within each category section, required fields
	 * come before optional. For a single category with no parents, no
	 * redundant category heading is rendered.
	 *
	 * @param CategoryModel[] $inheritanceChain C3-linearized chain [child, parent1, ..., root]
	 * @return array Lines of wikitext
	 */
	private function generateGroupedPropertyFields( array $inheritanceChain ): array {
		$out = [];
		$hasInheritance = count( $inheritanceChain ) > 1;

		$propertyOwner = InheritanceResolver::buildPropertyOwnerMap( $inheritanceChain );

		// Iterate root-first
		foreach ( array_reverse( $inheritanceChain ) as $chainCat ) {
			$catName = $chainCat->getName();

			// Collect properties owned by this category
			$ownRequired = [];
			$ownOptional = [];
			foreach ( $propertyOwner as $prop => $owner ) {
				if ( $owner !== $catName ) {
					continue;
				}
				if ( in_array( $prop, $chainCat->getRequiredProperties(), true ) ) {
					$ownRequired[] = $prop;
				} else {
					$ownOptional[] = $prop;
				}
			}

			sort( $ownRequired );
			sort( $ownOptional );

			if ( empty( $ownRequired ) && empty( $ownOptional ) ) {
				continue;
			}

			// Category heading (only when there's actual inheritance)
			if ( $hasInheritance ) {
				$catLabel = $chainCat->getLabel();
				$out[] = "=== $catLabel ===";
				$out[] = '';
			}

			// Single table for this category's fields (required first, then optional)
			$out[] = '{| class="formtable"';

			foreach ( $ownRequired as $prop ) {
				$out = array_merge( $out, $this->generateTableField( $prop, $chainCat, true ) );
			}
			foreach ( $ownOptional as $prop ) {
				$out = array_merge( $out, $this->generateTableField( $prop, $chainCat, false ) );
			}

			$out[] = '|}';
			$out[] = '';
		}

		return $out;
	}

	/**
	 * Generate a table row for a property field with label and description.
	 */
	private function generateTableField(
		string $propertyName,
		CategoryModel $category,
		?bool $isRequired = null
	): array {
		$prop = $this->propertyStore->readProperty( $propertyName )
			?: new PropertyModel( $propertyName, [ 'datatype' => 'Page' ] );

		$isReq = ( $isRequired !== null )
			? $isRequired
			: in_array( $propertyName, $category->getRequiredProperties(), true );

		$param = NamingHelper::propertyToParameter( $propertyName );
		$label = $this->s( $prop->getLabel() );
		$description = $this->s( $prop->getDescription() );

		// Special handling for "Has Type" property
		if ( strcasecmp( $propertyName, 'Has type' ) === 0 ) {
			$input = $this->generateHasTypeInput( $isReq );
		} else {
			$input = $this->inputMapper->generateInputDefinition( $prop, $isReq );
		}

		$out = [];
		$out[] = '|-';

		$labelLine = '! ' . $label;
		if ( $isReq ) {
			$labelLine .= '<span style="color:red;"> *</span>';
		}
		$labelLine .= ':';
		if ( $description !== '' ) {
			$labelLine .= ' <br><p class="pfFieldDescription" style="font-size:0.7em; color:gray;">'
				. htmlspecialchars( $description, ENT_QUOTES )
				. '</p>';
		}
		$out[] = $labelLine;

		$out[] = '| {{{field|' . $param . '|property=' . $this->s( $prop->getName() ) . '|' . $input . '}}}';

		return $out;
	}

	/**
	 * Generate input definition for "Has Type" property with SMW datatypes.
	 */
	private function generateHasTypeInput( bool $isRequired ): string {
		$datatypes = PropertyModel::getValidDatatypes();

		$params = [
			'input type=dropdown',
			'values=' . implode( ',', $datatypes )
		];

		if ( $isRequired ) {
			$params[] = 'mandatory=true';
		}

		return implode( '|', $params );
	}

	/**
	 * Subobject repeatable blocks collected from the full inheritance chain.
	 *
	 * @param CategoryModel[] $inheritanceChain
	 * @return array Lines of wikitext
	 */
	private function generateSubobjectSectionsFromChain( array $inheritanceChain ): array {
		// Collect subobjects from all categories in the chain, deduped
		$all = [];
		foreach ( $inheritanceChain as $cat ) {
			foreach ( $cat->getRequiredSubobjects() as $n ) {
				if ( !isset( $all[$n] ) ) {
					$all[$n] = true;
				}
			}
			foreach ( $cat->getOptionalSubobjects() as $n ) {
				if ( !isset( $all[$n] ) ) {
					$all[$n] = false;
				}
			}
		}

		if ( empty( $all ) ) {
			return [];
		}

		// Use the leaf category for field generation context
		$leafCategory = $inheritanceChain[0];

		$out = [];

		foreach ( $all as $subName => $isRequired ) {

			$model = $this->subobjectStore->readSubobject( $subName );
			if ( !$model instanceof SubobjectModel ) {
				wfLogWarning( "SemanticSchemas: Missing Subobject:$subName" );
				continue;
			}

			$label = $this->s( $model->getLabel() ?: $model->getName() );

			$out[] = '=== ' . $label . ' ===';
			$out[] = '';

			$templateName = 'Subobject/' . $this->s( $model->getName() );

			$for = [ $templateName, 'multiple' ];
			if ( $isRequired ) {
				$for[] = 'minimum instances=1';
			}

			$out[] = '{{{for template|' . implode( '|', $for ) . '}}}';
			$out[] = '';

			// Generate table for subobject properties
			$subProps = array_merge( $model->getRequiredProperties(), $model->getOptionalProperties() );
			if ( !empty( $subProps ) ) {
				$out[] = '{| class="formtable"';

				foreach ( $subProps as $p ) {
					$must = $model->isPropertyRequired( $p );
					$out = array_merge( $out, $this->generateTableField( $p, $leafCategory, $must ) );
				}

				$out[] = '|}';
				$out[] = '';
			}

			$out[] = '{{{end template}}}';
			$out[] = '';
		}

		return $out;
	}

	/* ----------------------------------------------------------
	 * Save + utilities
	 * -------------------------------------------------------- */

	public function updateForm( string $name, string $content ): bool {
		if ( trim( $name ) === '' ) {
			throw new \InvalidArgumentException( "Form name cannot be empty" );
		}

		$title = $this->pageCreator->makeTitle( $name, \PF_NS_FORM );
		if ( !$title ) {
			wfLogWarning( "SemanticSchemas: Unable to create form title for '$name'" );
			return false;
		}

		$result = $this->pageCreator->createOrUpdatePage(
			$title,
			$content,
			'SemanticSchemas: Auto-generated form'
		);

		// Purge the form page cache to ensure PageForms sees the updated content.
		// This is critical when form parameters like namespace targeting change,
		// as PageForms caches the parsed form definition.
		if ( $result ) {
			$this->pageCreator->purgePage( $title );
		}

		return $result;
	}

	/**
	 * @param CategoryModel $leafCategory The leaf (unmerged) category
	 * @param CategoryModel[] $inheritanceChain C3-linearized chain [child, parent1, ..., root]
	 * @return bool
	 */
	public function generateAndSaveForm( CategoryModel $leafCategory, array $inheritanceChain = [] ): bool {
		try {
			$txt = $this->generateForm( $leafCategory, $inheritanceChain );
			return $this->updateForm( $leafCategory->getName(), $txt );
		} catch ( \Exception $e ) {
			wfLogWarning(
				"SemanticSchemas: Failed to gen/save form for {$leafCategory->getName()}: "
				. $e->getMessage()
			);
			return false;
		}
	}

	public function formExists( string $categoryName ): bool {
		$t = $this->pageCreator->makeTitle( $categoryName, \PF_NS_FORM );
		return $t && $this->pageCreator->pageExists( $t );
	}

	/**
	 * Find the parent category property from a list of property names.
	 *
	 * @param array $props List of property names
	 * @return string|null
	 */
	private function findParentCategoryPropertyFromList( array $props ): ?string {
		foreach ( $props as $p ) {
			$lc = strtolower( $p );

			if (
				str_contains( $lc, 'parent' ) &&
				str_contains( $lc, 'category' )
			) {
				return $p;
			}
		}

		return null;
	}
}
