<?php

namespace MediaWiki\Extension\SemanticSchemas\Generator;

use InvalidArgumentException;
use MediaWiki\Extension\SemanticSchemas\Schema\CategoryModel;
use MediaWiki\Extension\SemanticSchemas\Schema\EffectiveCategoryModel;
use MediaWiki\Extension\SemanticSchemas\Schema\FieldModel;
use MediaWiki\Extension\SemanticSchemas\Schema\InheritanceResolver;
use MediaWiki\Extension\SemanticSchemas\Schema\PropertyModel;
use MediaWiki\Extension\SemanticSchemas\Store\PageCreator;
use MediaWiki\Extension\SemanticSchemas\Store\WikiPropertyStore;
use MediaWiki\Extension\SemanticSchemas\Util\Constants;
use MediaWiki\Extension\SemanticSchemas\Util\NamingHelper;
use MediaWiki\Language\Language;
use Mediawiki\Title\Title;

/**
 * Generates core templates for proper Semantic MediaWiki functioning.
 *
 * Responsibilities:
 *   - Semantic template: Template:<Category>/semantic (stores data via #set)
 *   - Dispatcher template: Template:<Category> (coordinates form, storage, and display)
 *   - Subobject templates: Template:<Name>/subobject (for nested data via #subobject)
 */
class TemplateGenerator {

	private PageCreator $pageCreator;
	private WikiPropertyStore $propertyStore;
	private Language $language;

	public function __construct(
		PageCreator $pageCreator,
		WikiPropertyStore $propertyStore,
		Language $language
	) {
		$this->pageCreator = $pageCreator;
		$this->propertyStore = $propertyStore;
		$this->language = $language;
	}

	/* =====================================================================
	 * PROPERTY VALUE EXPRESSION HELPER
	 * ===================================================================== */

	/**
	 * Generate the property line for a semantic template's #set block.
	 *
	 * For Page-type properties with allowedNamespace, this adds the namespace
	 * prefix to ensure SMW correctly interprets values as page references.
	 *
	 * This is necessary because Page Forms autocomplete returns bare page names
	 * without namespace prefixes (e.g. "MyPage" instead of "Project:MyPage").
	 * The template must re-add the prefix so SMW stores the fully qualified
	 * page reference.
	 *
	 * @param string $propertyName The SMW property name
	 * @param string $param The template parameter name
	 * @return string The wikitext for the property line
	 */
	private function generatePropertyLine( string $propertyName, string $param ): string {
		$propModel = $this->propertyStore->readProperty( $propertyName );

		// Default: simple property = value
		if ( !$propModel instanceof PropertyModel ) {
			return ' | ' . $propertyName . ' = {{{' . $param . '|}}}';
		}

		$allowedNamespace = $propModel->getAllowedNamespace();

		// Non-Page or no namespace restriction: simple assignment
		if ( !$propModel->isPageType() || $allowedNamespace === null || $allowedNamespace === '' ) {
			if ( $propModel->allowsMultipleValues() ) {
				return ' | ' . $propertyName . ' = {{{' . $param . '|}}} |+sep=,';
			}
			return ' | ' . $propertyName . ' = {{{' . $param . '|}}}';
		}

		if ( $propModel->allowsMultipleValues() ) {
			// Multi-value Page property with namespace: prefix each value via #arraymap.
			// Only add namespace when the value has none (FULLPAGENAME == PAGENAME).
			return ' | ' . $propertyName . ' = {{#arraymap:{{{' . $param .
				'|}}}|,|@@item@@|{{#ifeq:{{FULLPAGENAME:@@item@@}}|{{PAGENAME:@@item@@}}|'
				. $allowedNamespace . ':}}@@item@@|,}} |+sep=,';
		}

		// Single value: add namespace only when the value has none.
		// If the user typed a namespace explicitly, preserve it as-is.
		return ' | ' . $propertyName . ' = {{#if:{{{' . $param . '|}}}|'
			. '{{#ifeq:{{FULLPAGENAME:{{{' . $param . '|}}}}}|{{PAGENAME:{{{' . $param . '|}}}}}'
			. '|' . $allowedNamespace . ':}}{{{' . $param . '|}}}|}}';
	}

	/* =====================================================================
	 * SEMANTIC TEMPLATE  (Template:<Category>/semantic)
	 * ===================================================================== */

	/**
	 * Generate content for the semantic template.
	 *
	 * Each semantic template calls its direct parents' semantic templates
	 * (forwarding effective props) and stores its own properties via #set.
	 *
	 * @param CategoryModel $category The raw (own-properties-only) category
	 * @param array<string,EffectiveCategoryModel> $parentEffectives Parent name → effective model
	 * @return string
	 */
	public function generateSemanticTemplate(
		CategoryModel $category,
		array $parentEffectives = []
	): string {
		$name = trim( $category->getName() );
		if ( $name === '' ) {
			throw new InvalidArgumentException( "Category name cannot be empty" );
		}

		$fields = $category->getPropertyFields();
		self::sortFieldsByName( $fields );

		$out = [];
		$out[] = '<noinclude>';
		$out[] = '<!-- AUTO-GENERATED by SemanticSchemas - DO NOT EDIT MANUALLY -->';
		$out[] = 'Semantic template for Category:' . $name;
		// Strip-newline wrap: each parser-function/template call below is bracketed
		// with HTML strip comments (<!--\n-->) so the source's structural newlines
		// don't leak into the page when transcluded. Without this, the inheritance
		// chain (e.g. Meeting -> Event -> Activity) accumulates one blank line per
		// level above any visible content on every page in that category.
		$out[] = '</noinclude><includeonly><!--';

		/* Embed parent semantic templates */
		foreach ( $parentEffectives as $parentName => $parentEffective ) {
			$parentFields = $parentEffective->getPropertyFields();
			self::sortFieldsByName( $parentFields );
			$callLines = $this->generateTemplateCall( $parentName . '/semantic', $parentFields );
			$callLines[0] = '-->' . $callLines[0];
			$callLines[count( $callLines ) - 1] .= '<!--';
			$out = array_merge( $out, $callLines );
		}

		/* Own property storage */
		$out[] = '-->{{#set:';

		foreach ( $fields as $field ) {
			$out[] = $this->generatePropertyLine(
				$field->getName(), $field->getParameterName()
			);
		}

		$out[] = '}}<!--';

		$out[] = '--></includeonly>';

		return implode( "\n", $out );
	}

	/* =====================================================================
	 * DISPATCHER TEMPLATE (Template:<Category>)
	 * ===================================================================== */

	/**
	 * Generate a template call with property parameter passthrough.
	 *
	 * @param string $templateName Template name to call
	 * @param FieldModel[] $fields
	 * @return array Lines of wikitext
	 */
	private function generateTemplateCall( string $templateName, array $fields ): array {
		$out = [];
		$out[] = '{{' . $templateName;
		foreach ( $fields as $field ) {
			$param = $field->getParameterName();
			$out[] = ' | ' . $param . ' = {{{' . $param . '|}}}';
		}
		$out[] = '}}';
		return $out;
	}

	/**
	 * Generate content for the dispatcher template.
	 *
	 * @param EffectiveCategoryModel $effective The fully merged category
	 * @param ?InheritanceResolver $resolver Used to resolve effective
	 *        property fields on this category's subobject-type children so
	 *        the dispatcher can emit inline `#ask` sections that project
	 *        values directly into per-subcat `/subobject-row` templates.
	 *        When null, the dispatcher emits no inline subobject sections
	 *        and Category/table's dynamic fallback handles them.
	 * @return string
	 */
	public function generateDispatcherTemplate(
		EffectiveCategoryModel $effective,
		?InheritanceResolver $resolver = null
	): string {
		$name = trim( $effective->getName() );
		if ( $name === '' ) {
			throw new InvalidArgumentException( "Category name cannot be empty" );
		}

		$allFields = $effective->getPropertyFields();
		self::sortFieldsByName( $allFields );

		$out = [];
		$out[] = '<noinclude>';
		$out[] = '<!-- AUTO-GENERATED by SemanticSchemas - DO NOT EDIT MANUALLY -->';
		$out[] = 'Dispatcher template for Category:' . $name;
		$out[] = '</noinclude><includeonly>{{#default_form:' . $name . '}}';

		/* Semantic storage */
		$out = array_merge( $out, $this->generateTemplateCall( $name . '/semantic', $allFields ) );

		/* Dynamic display — Category/table (or user override) handles properties
		 * in wikitext; subobject sections are emitted inline below when a
		 * resolver is available. */
		$out = array_merge( $out, $this->generateDynamicDisplay( $effective, $allFields, $resolver ) );

		if ( $resolver !== null ) {
			$out = array_merge( $out, $this->generateSubobjectSections( $effective, $resolver ) );
		}

		/* Category membership */
		// FIXME: Temporary special-case hack to exclude Category:Category membership -
		// prevent categories from inheriting the metacategory fields.
		// To be resolved in #122 after removing the need for generation,
		// where this will become part of the pre-packaged Template:Category template
		$categoryPrefix = $this->language->getFormattedNsText( NS_CATEGORY );
		if ( $name !== $categoryPrefix ) {
			$out[] = '[[' . $categoryPrefix . ':' . $name . ']]';
		}
		$out[] = '[[Category:' . Constants::SEMANTICSCHEMAS_MANAGED_CATEGORY . ']]';

		$out[] = '</includeonly>';

		return implode( "\n", $out );
	}

	/**
	 * Generate the dynamic display section of the dispatcher template.
	 *
	 * Emits a call to the appropriate format template (Category/table,
	 * Category/sidebox, etc.) with the effective schema baked in as
	 * params: category label, ordered property list, and per-property
	 * label + value expression. The format template's fast path iterates
	 * these without issuing SMW queries.
	 *
	 * When a resolver is passed, also sets `subobjects=no` on the format
	 * template call so its Category/subobjects block is suppressed —
	 * dispatcher emits per-subcat baked `#ask` sections inline instead.
	 *
	 * When the category has a custom display template, that is called
	 * after the format template (or alone if format=none).
	 *
	 * @param EffectiveCategoryModel $effective
	 * @param FieldModel[] $allFields Sorted effective fields
	 * @param ?InheritanceResolver $resolver
	 * @return string[]
	 */
	private function generateDynamicDisplay(
		EffectiveCategoryModel $effective,
		array $allFields,
		?InheritanceResolver $resolver = null
	): array {
		$name = $effective->getName();
		$format = $effective->getDisplayFormat() ?? 'table';
		$out = [];

		if ( $format !== 'none' ) {
			$out[] = '{{Category/' . $format;
			$out[] = ' | category=' . $name;
			$out[] = ' | label=' . $effective->getLabel();
			if ( $resolver !== null ) {
				// Dispatcher handles subobjects inline via projected #ask; suppress
				// Category/table's nested Category/subobjects block. Backlinks
				// default falls back to subobjects, so set explicitly: yes when
				// the category declares backlink properties, no otherwise —
				// skipping Category/render-reverse's initial #show lookup.
				$out[] = ' | subobjects=no';
				$out[] = ' | backlinks=' . ( $effective->getBacklinksFor() === [] ? 'no' : 'yes' );
			}
			$out = array_merge( $out, $this->buildBakedPropLines( $allFields ) );
			$out = array_merge( $out, $this->buildBakedBacklinkLines( $effective ) );
			$out[] = '}}';
		}

		$displayTemplate = $effective->getDisplayTemplate();
		if ( $displayTemplate !== null ) {
			$templateName = preg_replace( '/^Template:/', '', $displayTemplate );
			$out = array_merge( $out, $this->generateTemplateCall( $templateName, $allFields ) );
		}

		return $out;
	}

	/**
	 * Build the `props=...` / `label_<p>=...` / `val_<p>=...` lines shared
	 * by the dispatcher's format-template call and by each per-category
	 * `/subobject-row` template. Skips fields whose property is marked
	 * `Is hidden = true`.
	 *
	 * @param FieldModel[] $fields
	 * @return string[]
	 */
	private function buildBakedPropLines( array $fields ): array {
		$paramNames = [];
		$labelLines = [];
		$valueLines = [];
		foreach ( $fields as $field ) {
			$propName = $field->getName();
			$prop = $this->propertyStore->readProperty( $propName );
			if ( $prop instanceof PropertyModel && $prop->isHidden() ) {
				continue;
			}
			$param = $field->getParameterName();
			$paramNames[] = $param;
			$labelLines[] = ' | label_' . $param . '=' . $this->resolvePropertyLabel( $propName );
			$valueLines[] = ' | val_' . $param . '='
				. $this->buildRenderedValueExpression( $field, $prop, $param );
		}
		if ( $paramNames === [] ) {
			return [];
		}
		return array_merge(
			[ ' | props=' . implode( ',', $paramNames ) ],
			$labelLines,
			$valueLines
		);
	}

	/**
	 * Emitted inline rather than via per-property `backlink_label_<p>=`
	 * params because `#arraymap` parameter forwarding through
	 * `Category/sidebox` -> `Category/render-reverse` doesn't reliably
	 * preserve `{{{name|default}}}` lookups across the nested call.
	 *
	 * @return string[]
	 */
	private function buildBakedBacklinkLines( EffectiveCategoryModel $effective ): array {
		$props = $effective->getBacklinksFor();
		if ( $props === [] ) {
			return [];
		}
		$countTerms = [];
		$rowCalls = [];
		foreach ( $props as $propName ) {
			$countTerms[] = '+{{#ask:[[' . $propName
				. '::{{FULLPAGENAME}}]]|format=count}}';
			$rowCalls[] = '{{Category/backlink-row|prop=' . $propName
				. '|label=' . $this->resolveInversePropertyLabel( $propName ) . '}}';
		}
		$section = '{{#ifexpr: 0' . implode( '', $countTerms ) . " > 0 |{{!}}-\n"
			. '{{Category/backlinks-header}}' . "\n"
			. implode( "\n", $rowCalls ) . '}}';
		return [ ' | backlink_section=' . $section ];
	}

	/**
	 * Apply the field's render template (or the auto-selected default for
	 * Page-typed properties) around the namespace-prefixed value expression.
	 *
	 * Priority:
	 *   1. Field's explicit `Has render template` annotation.
	 *   2. `Property/Page` for Page-typed properties (so page values render
	 *      as wikilinks — matches main-branch DisplayStubGenerator
	 *      behavior, which auto-selected Property/Page in PropertyModel).
	 *   3. No wrapper (bare value expression).
	 */
	private function buildRenderedValueExpression(
		FieldModel $field,
		?PropertyModel $prop,
		string $paramName
	): string {
		$valueExpr = $this->buildDisplayValueExpression( $field->getName(), $paramName );
		$renderTpl = $field->getRenderTemplate();
		if ( $renderTpl === null && $prop instanceof PropertyModel && $prop->isPageType() ) {
			$renderTpl = 'Property/Page';
		}
		if ( $renderTpl === null ) {
			return $valueExpr;
		}
		return '{{' . $renderTpl . ' | value=' . $valueExpr . ' }}';
	}

	/**
	 * Emit inline `#ask` sections for each subobject-type child of this
	 * category. Each section projects the subcat's effective (visible)
	 * property values as named template args into the subcat's
	 * `/subobject-row` template, which renders one instance via
	 * Category/table's fast path.
	 *
	 * Sort by `Has sort order` is only added when the subcat's effective
	 * field list declares it — otherwise SMW silently drops subobjects
	 * without a sort value.
	 *
	 * @return string[]
	 */
	private function generateSubobjectSections(
		EffectiveCategoryModel $effective,
		InheritanceResolver $resolver
	): array {
		$subFields = $effective->getSubobjectFields();
		if ( $subFields === [] ) {
			return [];
		}

		$out = [];
		$categoryPrefix = $this->language->getFormattedNsText( NS_CATEGORY );

		foreach ( $subFields as $subField ) {
			$subName = $subField->getName();
			if ( !$resolver->hasCategory( $subName ) ) {
				continue;
			}
			$sub = $resolver->getEffectiveCategory( $subName );
			$fields = $sub->getPropertyFields();
			self::sortFieldsByName( $fields );

			$hasSortOrder = false;
			$projections = [];
			foreach ( $fields as $f ) {
				if ( $f->getName() === 'Has sort order' ) {
					$hasSortOrder = true;
				}
				$prop = $this->propertyStore->readProperty( $f->getName() );
				if ( $prop instanceof PropertyModel && $prop->isHidden() ) {
					continue;
				}
				$projections[] = ' | ?' . $f->getName() . '=' . $f->getParameterName();
			}

			$label = $sub->getLabel();
			$out[] = '=== ' . $label . ' ===';
			$out[] = '{{#ask: [[-Has subobject::{{FULLPAGENAME}}]] [['
				. $categoryPrefix . ':' . $subName . ']]';
			$out = array_merge( $out, $projections );
			$out[] = ' | format=template';
			$out[] = ' | template=' . $subName . '/subobject-row';
			$out[] = ' | named args=yes';
			$out[] = ' | link=none';
			$out[] = ' | mainlabel=-';
			if ( $hasSortOrder ) {
				$out[] = ' | sort=Has sort order';
				$out[] = ' | order=asc';
			}
			$out[] = '}}';
		}

		return $out;
	}

	/**
	 * Generate the `/subobject-row` template for a category.
	 *
	 * Wraps Category/table with baked props from the effective model and
	 * `subobjects=no | backlinks=no`. Values arrive as named params from
	 * SMW's `format=template | ?Prop=alias | named args=yes` projection
	 * used by generateSubobjectSections.
	 */
	private function generateSubobjectRowTemplate( EffectiveCategoryModel $sub ): string {
		$name = $sub->getName();
		$fields = $sub->getPropertyFields();
		self::sortFieldsByName( $fields );

		$out = [];
		$out[] = '<noinclude>';
		$out[] = '<!-- AUTO-GENERATED by SemanticSchemas - DO NOT EDIT MANUALLY -->';
		$out[] = 'Subobject-row template for Category:' . $name;
		$out[] = '</noinclude><includeonly>{{Category/table';
		$out[] = ' | category=' . $name;
		$out[] = ' | label=' . $sub->getLabel();
		$out[] = ' | subobjects=no';
		$out[] = ' | backlinks=no';
		$out = array_merge( $out, $this->buildBakedPropLines( $fields ) );
		$out[] = '}}</includeonly>';

		return implode( "\n", $out );
	}

	/**
	 * Resolve a property's display label, falling back to a generated
	 * label when the property has no wiki page yet.
	 */
	private function resolvePropertyLabel( string $propertyName ): string {
		$prop = $this->propertyStore->readProperty( $propertyName );
		if ( $prop instanceof PropertyModel ) {
			return $prop->getLabel();
		}
		return NamingHelper::generatePropertyLabel( $propertyName );
	}

	/**
	 * Resolve a property's inverse-label (used in backlink headings),
	 * falling back to the property name when no Inverse property label
	 * is set.
	 */
	private function resolveInversePropertyLabel( string $propertyName ): string {
		$prop = $this->propertyStore->readProperty( $propertyName );
		if ( $prop instanceof PropertyModel && $prop->getInverseLabel() !== '' ) {
			return $prop->getInverseLabel();
		}
		return $propertyName;
	}

	/**
	 * Build the display-value expression forwarded as `val_<param>` to a
	 * format template. For Page-typed properties with an allowed namespace,
	 * re-adds the namespace prefix when the form value is missing one so
	 * displayed links resolve correctly — matches the main-branch
	 * DisplayStubGenerator behavior. Idempotent: values that already have
	 * a namespace are passed through unchanged.
	 */
	private function buildDisplayValueExpression( string $propertyName, string $paramName ): string {
		$prop = $this->propertyStore->readProperty( $propertyName );
		if ( !$prop instanceof PropertyModel ) {
			return '{{{' . $paramName . '|}}}';
		}

		$allowedNamespace = $prop->getAllowedNamespace();
		if ( !$prop->isPageType() || $allowedNamespace === null || $allowedNamespace === '' ) {
			return '{{{' . $paramName . '|}}}';
		}

		if ( $prop->allowsMultipleValues() ) {
			return '{{#arraymap:{{{' . $paramName . '|}}}|,|@@item@@|'
				. '{{#ifeq:{{FULLPAGENAME:@@item@@}}|{{PAGENAME:@@item@@}}|'
				. $allowedNamespace . ':}}@@item@@|,&#32;}}';
		}

		return '{{#if:{{{' . $paramName . '|}}}|'
			. '{{#ifeq:{{FULLPAGENAME:{{{' . $paramName . '|}}}}}|{{PAGENAME:{{{' . $paramName . '|}}}}}'
			. '|' . $allowedNamespace . ':}}{{{' . $paramName . '|}}}|}}';
	}

	/* =====================================================================
	 * SUBOBJECT TEMPLATES  (Template:<Name>/subobject)
	 *
	 * Subobject types are categories. The generator reads the category schema
	 * via WikiCategoryStore and produces templates that use SMW's @category
	 * mechanism for typing instead of a custom property.
	 * ===================================================================== */

	private function generateSubobjectTemplate( EffectiveCategoryModel $sub ): string {
		$name = $sub->getName();

		$out = [];
		$out[] = '<noinclude>';
		$out[] = '<!-- AUTO-GENERATED by SemanticSchemas - DO NOT EDIT MANUALLY -->';
		$out[] = 'Subobject template for Category:' . $name;
		// Strip-newline wrap: the #subobject call returns empty but the
		// surrounding source newlines would otherwise leak one blank line
		// per call into the page. Pages with N subobject instances
		// (e.g. timeline steps) accumulated N blank lines above any
		// content rendered after them.
		$out[] = '</noinclude><includeonly><!--';

		$idPrefix = NamingHelper::propertyToParameter( $name );
		$out[] = '-->{{#subobject:';
		$out[] = ' |@category=' . $name;
		$out[] = ' | Has sort order = {{#s2counter:' . $idPrefix . '}}';

		foreach ( $sub->getPropertyFields() as $field ) {
			$out[] = $this->generatePropertyLine(
				$field->getName(), $field->getParameterName()
			);
		}

		$out[] = '}}<!--';
		$out[] = '--></includeonly>';

		return implode( "\n", $out );
	}

	/* =====================================================================
	 * PUBLIC: FULL TEMPLATE GENERATION ENTRYPOINT
	 * ===================================================================== */

	/**
	 * Generate all artifacts for a category (semantic, dispatcher, subobjects).
	 *
	 * @param CategoryModel $category The raw (own-properties-only) category
	 * @param InheritanceResolver $resolver
	 * @return array{success: bool, errors: string[]}
	 */
	public function generateAllTemplates(
		CategoryModel $category,
		InheritanceResolver $resolver
	): array {
		$errors = [];
		$name = $category->getName();
		$effective = $resolver->getEffectiveCategory( $name );

		/* Semantic template — own declared properties only */
		try {
			$parentEffectives = $resolver->getParentEffectiveModels( $name );
			$content = $this->generateSemanticTemplate( $category, $parentEffectives );
			$this->updateTemplate( $name . '/semantic', $content );
		} catch ( \Exception $e ) {
			$errors[] = "Error generating semantic template for $name: " . $e->getMessage();
		}

		/* Dispatcher */
		try {
			$content = $this->generateDispatcherTemplate( $effective, $resolver );
			$this->updateTemplate( $name, $content );
		} catch ( \Exception $e ) {
			$errors[] = "Error generating dispatcher template for $name: " . $e->getMessage();
		}

		/* Subobject template — every category gets one */
		try {
			$content = $this->generateSubobjectTemplate( $effective );
			$this->updateTemplate( $name . '/subobject', $content );
		} catch ( \Exception $e ) {
			$errors[] = "Error generating subobject template for $name: " . $e->getMessage();
		}

		/* Subobject-row template — every category gets one; referenced by
		 * parent categories' dispatchers when this category is used as a
		 * subobject type. Cheap to emit even for categories that are only
		 * ever top-level. */
		try {
			$content = $this->generateSubobjectRowTemplate( $effective );
			$this->updateTemplate( $name . '/subobject-row', $content );
		} catch ( \Exception $e ) {
			$errors[] = "Error generating subobject-row template for $name: " . $e->getMessage();
		}

		return [
			'success' => !$errors,
			'errors' => $errors
		];
	}

	/**
	 * Sort FieldModel[] alphabetically by name (in place).
	 *
	 * @param FieldModel[] &$fields
	 */
	private static function sortFieldsByName( array &$fields ): void {
		usort( $fields, static fn ( FieldModel $a, FieldModel $b ) =>
			strcmp( $a->getName(), $b->getName() )
		);
	}

	/* =====================================================================
	 * BASIC UPDATE WRAPPER
	 * ===================================================================== */

	private function updateTemplate( string $name, string $content ): bool {
		if ( trim( $name ) === '' ) {
			throw new InvalidArgumentException( "Template name cannot be empty" );
		}

		$title = Title::makeTitleSafe( NS_TEMPLATE, $name );
		if ( !$title ) {
			wfLogWarning( "SemanticSchemas: Failed to create Title for template '$name'" );
			return false;
		}

		return $this->pageCreator->createOrUpdatePage(
			$title,
			$content,
			'SemanticSchemas: Auto-generated template'
		);
	}

	/**
	 * Check if the semantic template exists for a category.
	 *
	 * @param string $categoryName
	 * @return bool
	 */
	public function semanticTemplateExists( string $categoryName ): bool {
		$templateName = trim( $categoryName ) . '/semantic';
		$title = Title::makeTitleSafe( NS_TEMPLATE, $templateName );
		return $title && $title->exists();
	}
